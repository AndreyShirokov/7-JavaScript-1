let age = 18;
age += 2; // age = age + 2
age -= 3; // age = age - 3

age *= 3; // age = age * 2
age /= 2; // age = age / 3

age++ // age = age + 1
age-- // age = age - 1

console.log(age);

// операторы сравнения
const vasia = 20; // Васе 20 лет
console.log(age > vasia); // Вася моложе
console.log(age >= vasia); 
console.log(age < vasia); 
console.log(age <= vasia); 
console.log(age == vasia); 
